/*

Estrutura if

if (condição) {
    ação que será realizada se condição verdadeira
} else {
    ação que será realizada se a primeira condição não for verdadeira
}

*/

const hora = 12;

// hora menor ou igual a
if (hora <= 12) {
    console.log('Bom dia');
} else {
    console.log('Olá, bem vindo!');
};

// hora maior que 13
if (hora > 13) {
    console.log('Bom dia');
} else {
    console.log('Olá, bem vindo!');
};